    private void btnCapNhapMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCapNhapMouseClicked

        if (txtDrinkandFood.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Drink and Food ko duoc de trong");
            return;
        }
        if (JOptionPane.showConfirmDialog(this, "Bán có muốn cập nhập lại không ?") == JOptionPane.NO_OPTION) {
            return;
        }
        try {
            ModelLogin mdl = new ModelLogin();
            mdl.setDrinkAndFood(txtDrinkandFood.getText());
            mdl.setSizeM(Integer.parseInt(txtSizeM.getText()));
            mdl.setSizeL(Integer.parseInt(txtSizeL.getText()));
            mdl.setImage(personalImage);

            DaoQl_Menu daoQ = new DaoQl_Menu();
            if (daoQ.update(mdl)) {
                JOptionPane.showMessageDialog(this, "Drink and Food đã được cập nhập");
                loadDataToMenu();
            } else {
                JOptionPane.showMessageDialog(this, "Drink and Food chưa cập nhập");
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_btnCapNhapMouseClicked

