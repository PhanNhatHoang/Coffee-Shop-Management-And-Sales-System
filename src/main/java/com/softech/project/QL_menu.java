    private void btnAddImgActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddImgActionPerformed
       JFileChooser chooser = new JFileChooser();
       chooser.setFileFilter(new FileFilter() {
           @Override
           public boolean accept(File f) {
               if (f.isDirectory()) {
                   return true;
               } else {
                   return f.getName().toLowerCase().endsWith(".jpg");
               }
           }

           @Override
           public String getDescription() {
               return "Image File (*.jpg)";
           }
       });
        if (chooser.showOpenDialog(parentl)== JFileChooser.CANCEL_OPTION) {
            return;
        }
        
        File file = chooser.getSelectedFile();
        try {
            ImageIcon icon = new ImageIcon(file.getPath());
            Image img = ImageHelper.resize(icon.getImage(), 141, 141);
            ImageIcon resizeIcon = new ImageIcon(img);
            lblIamge.setIcon(resizeIcon);
            personalImage = ImageHelper.toByteArray(img, "jpg");
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_btnAddImgActionPerformed

